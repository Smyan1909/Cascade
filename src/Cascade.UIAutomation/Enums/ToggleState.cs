namespace Cascade.UIAutomation.Enums;

/// <summary>
/// The toggle state of a UI element that supports the Toggle pattern.
/// </summary>
public enum ToggleState
{
    /// <summary>
    /// The element is not toggled (unchecked).
    /// </summary>
    Off = 0,

    /// <summary>
    /// The element is toggled (checked).
    /// </summary>
    On = 1,

    /// <summary>
    /// The element is in an indeterminate state.
    /// </summary>
    Indeterminate = 2
}

